package mastermind;

public class Posicion {
    private int posX;
    private int posY;

    public Posicion() {}

    //Getters and Setters
   public int getX() {
        return posX;
   }
   public void setX(int X) {
        this.posX = posX;
   }
   public int getY() {
        return posY;
   }
   public void setY(int Y) {
        this.posY = posY;
   }

}
